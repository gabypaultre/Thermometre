GPIO_status GPIO_init(void)
{
    TRISAbits.TRISA4 = 0;
    LATAbits.LATA4 = 0;

    return GPIO_OK;
}

GPIO_status GPIO_setGpioHigh(void)
{
    LATAbits.LATA4 = 1;

    return GPIO_OK;
}

GPIO_status GPIO_setGpioLow(void)
{
    LATAbits.LATA4 = 0;

    return GPIO_OK;
}

